export function applyColorPalette(palette: ColorPalette, isDark: boolean = false): void {
  const theme = COLOR_PALETTES[palette];
  const colors = isDark ? theme.dark : theme.light;

  // Apply colors to CSS variables
  const root = document.documentElement;
  
  Object.entries(colors).forEach(([key, value]) => {
    // Convert camelCase to kebab-case
    const cssVarName = key.replace(/([A-Z])/g, '-$1').toLowerCase();
    root.style.setProperty(`--${cssVarName}`, value);
  });
}

export function getPaletteDisplayName(palette: ColorPalette): string {
  return palette.charAt(0).toUpperCase() + palette.slice(1);
}

